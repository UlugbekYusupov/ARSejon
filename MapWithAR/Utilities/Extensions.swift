extension Dictionary {
    init(plistNamed fileName: String, bundle: Bundle = Bundle.main) {
        guard
            let pList = bundle.path(forResource: fileName, ofType: "plist"),
            let dict = NSDictionary(contentsOfFile: pList)
            else { self = [:]; return }
        
        var result = [Key: Value]()
        dict.forEach { key, value in
            if let key = key as? Key, let value = value as? Value {
                result[key] = value
            }
        }
        self = result
    }
}

